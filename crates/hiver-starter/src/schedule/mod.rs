/// Scheduling configuration parsed from annotation attributes.
/// 从注解属性解析的调度配置。
///
/// Supports three scheduling modes, matching Spring's @Scheduled:
/// 支持三种调度模式，与 Spring 的 @Scheduled 一致：
///
/// - `Cron`: cron expression, e.g. `"0 0 * * *"`
/// - `FixedRate`: fixed interval from start-to-start, e.g. `"5s"`, `"5000ms"`
/// - `FixedDelay`: fixed delay from end-to-start, e.g. `"10s"`
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum ScheduleConfig {
    /// Cron expression, e.g. `"0 0 * * *"`.
    /// Cron 表达式。
    Cron(&'static str),

    /// Fixed rate (start-to-start interval), e.g. `"5s"`.
    /// 固定速率（从开始到开始的间隔）。
    FixedRate(&'static str),

    /// Fixed delay (end-to-start delay), e.g. `"10s"`.
    /// 固定延迟（从结束到开始的延迟）。
    FixedDelay(&'static str),
}

