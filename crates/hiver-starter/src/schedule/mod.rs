/// Parsed duration result with millisecond precision.
/// 解析后的时长结果，精度为毫秒。
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct ParsedDuration {
    /// Duration in milliseconds.
    /// 时长（毫秒）。
    pub millis: u64,
}

impl ParsedDuration {
    /// Create a new parsed duration from milliseconds.
    /// 从毫秒创建解析后的时长。
    pub fn from_millis(millis: u64) -> Self {
        Self { millis }
    }

    /// Create a new parsed duration from seconds.
    /// 从秒创建解析后的时长。
    pub fn from_secs(secs: u64) -> Self {
        Self {
            millis: secs * 1000,
        }
    }

    /// Create a new parsed duration from minutes.
    /// 从分钟创建解析后的时长。
    pub fn from_mins(mins: u64) -> Self {
        Self {
            millis: mins * 60 * 1000,
        }
    }
}

/// Parse a human-readable duration string into milliseconds.
/// 将人类可读的时长字符串解析为毫秒。
///
/// Supported formats:
/// 支持的格式：
///
/// - `"5000ms"` -> 5000 ms
/// - `"5s"` -> 5000 ms
/// - `"1m"` -> 60000 ms
/// - `"1h"` -> 3600000 ms
/// - `"5000"` -> 5000 ms (bare number treated as ms)
///
/// # Errors / 错误
///
/// Returns an error if the string cannot be parsed.
/// 如果字符串无法解析则返回错误。
pub fn parse_duration(input: &str) -> Result<ParsedDuration, ScheduleScanError> {
    let input = input.trim();

    if input.is_empty() {
        return Err(ScheduleScanError::InvalidConfig {
            detail: "duration string is empty".to_string(),
        });
    }

    // Try unit-suffix parsing first.
    // 首先尝试带单位后缀的解析。
    if input.ends_with("ms") {
        let num_str = &input[..input.len() - 2];
        let num: u64 = num_str
            .parse()
            .map_err(|_| ScheduleScanError::InvalidConfig {
                detail: format!("invalid milliseconds value: '{}'", num_str),
            })?;
        return Ok(ParsedDuration::from_millis(num));
    }

    if input.ends_with('s') {
        let num_str = &input[..input.len() - 1];
        let num: u64 = num_str
            .parse()
            .map_err(|_| ScheduleScanError::InvalidConfig {
                detail: format!("invalid seconds value: '{}'", num_str),
            })?;
        return Ok(ParsedDuration::from_secs(num));
    }

    if input.ends_with('m') {
        let num_str = &input[..input.len() - 1];
        let num: u64 = num_str
            .parse()
            .map_err(|_| ScheduleScanError::InvalidConfig {
                detail: format!("invalid minutes value: '{}'", num_str),
            })?;
        return Ok(ParsedDuration::from_mins(num));
    }

    if input.ends_with('h') {
        let num_str = &input[..input.len() - 1];
        let num: u64 = num_str
            .parse()
            .map_err(|_| ScheduleScanError::InvalidConfig {
                detail: format!("invalid hours value: '{}'", num_str),
            })?;
        return Ok(ParsedDuration::from_millis(num * 3600 * 1000));
    }

    // Bare number: treat as milliseconds.
    // 纯数字：当作毫秒处理。
    let num: u64 = input
        .parse()
        .map_err(|_| ScheduleScanError::InvalidConfig {
            detail: format!("invalid duration value: '{}'", input),
        })?;
    Ok(ParsedDuration::from_millis(num))
}

