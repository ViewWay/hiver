/// Scans for @Scheduled methods collected via `inventory` and registers them
/// with the TaskScheduler.
/// 扫描通过 `inventory` 收集的 @Scheduled 方法并将它们注册到 TaskScheduler。
///
/// This is the runtime counterpart to the compile-time `#[scheduled]` macro:
/// each annotated method emits a `ScheduledMethodDescriptor` via `inventory::submit!`,
/// and this scanner collects them during application startup.
///
/// 这是编译期 `#[scheduled]` 宏的运行时对应物：
/// 每个被注解的方法通过 `inventory::submit!` 发出一个 `ScheduledMethodDescriptor`，
/// 此扫描器在应用启动时收集它们。
pub struct ScheduledMethodScanner;

impl ScheduledMethodScanner {
    /// Create a new scanner.
    /// 创建新的扫描器。
    pub fn new() -> Self {
        Self
    }

    /// Scan all registered scheduled method descriptors and convert them
    /// into ScheduledTask instances.
    /// 扫描所有已注册的定时方法描述符并将它们转换为 ScheduledTask 实例。
    ///
    /// Returns a vector of (ScheduledTask, ScheduledFn) pairs ready for
    /// registration with the TaskScheduler.
    /// 返回一组 (ScheduledTask, ScheduledFn) 对，可直接注册到 TaskScheduler。
    pub fn scan(&self) -> Result<Vec<DiscoveredScheduledMethod>, ScheduleScanError> {
        let descriptors: Vec<&ScheduledMethodDescriptor> =
            inventory::iter::<ScheduledMethodDescriptor>.collect();

        let mut discovered = Vec::with_capacity(descriptors.len());

        for descriptor in descriptors {
            tracing::info!(
                "Discovered @Scheduled method: '{}' ({:?})",
                descriptor.name,
                descriptor.schedule_config,
            );

            let task = self.build_task(descriptor)?;
            discovered.push(DiscoveredScheduledMethod {
                task,
                task_fn: descriptor.task_fn.clone(),
            });
        }

        tracing::info!("Discovered {} @Scheduled method(s)", discovered.len());
        Ok(discovered)
    }

    /// Build a ScheduledTask from a descriptor.
    /// 从描述符构建 ScheduledTask。
    fn build_task(
        &self,
        descriptor: &ScheduledMethodDescriptor,
    ) -> Result<ScheduledTask, ScheduleScanError> {
        match &descriptor.schedule_config {
            ScheduleConfig::FixedRate(rate_str) => {
                let duration = parse_duration(rate_str)?;
                Ok(ScheduledTask::fixed_rate(descriptor.name, duration.millis))
            },
            ScheduleConfig::FixedDelay(delay_str) => {
                let duration = parse_duration(delay_str)?;
                Ok(ScheduledTask::fixed_delay(descriptor.name, duration.millis))
            },
            ScheduleConfig::Cron(cron_str) => {
                // Validate the cron expression.
                // 验证 cron 表达式。
                let _expr: CronExpression = cron_str.parse()?;
                Ok(ScheduledTask::cron(descriptor.name, *cron_str))
            },
        }
    }
}

impl Default for ScheduledMethodScanner {
    fn default() -> Self {
        Self::new()
    }
}

/// A discovered scheduled method ready for registration.
/// 一个已发现的、准备注册的定时方法。
pub struct DiscoveredScheduledMethod {
    /// The built scheduled task.
    /// 构建好的定时任务。
    pub task: ScheduledTask,

    /// The function to execute.
    /// 要执行的函数。
    pub task_fn: ScheduledFn,
}

