//! Schedule auto-configuration module / 定时任务自动配置模块
//!
//! Automatically configures scheduled task functionality.
//! 自动配置定时任务功能。
//!
//! # @Scheduled Method Scanning / @Scheduled 方法扫描
//!
//! This module scans for methods annotated with scheduling configuration
//! (cron, fixed_rate, fixed_delay) and registers them with the TaskScheduler.
//!
//! 此模块扫描带有调度配置（cron、fixed_rate、fixed_delay）的方法，
//! 并将它们注册到 TaskScheduler。
//!
//! # Design / 设计
//!
//! Uses `inventory` crate for compile-time collection of scheduled method
//! descriptors, consistent with the existing bean registration pattern.
//!
//! 使用 `inventory` crate 在编译期收集定时方法描述符，
//! 与现有 Bean 注册模式保持一致。

