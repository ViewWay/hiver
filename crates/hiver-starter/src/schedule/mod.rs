/// Compile-time descriptor for a @Scheduled method, submitted via `inventory::submit!`.
/// 编译期的 @Scheduled 方法描述符，通过 `inventory::submit!` 提交。
///
/// Procedural macros (e.g. `#[scheduled]`) populate this at compile time.
/// 过程宏（如 `#[scheduled]`）在编译期填充此结构。
///
/// # Example (generated by macro) / 示例（由宏生成）
///
/// ```rust,ignore
/// inventory::submit! {
///     ScheduledMethodDescriptor {
///         name: "cleanup_task",
///         schedule_config: ScheduleConfig::FixedRate("5s"),
///         task_fn: || { cleanup() },
///     }
/// }
/// ```
pub struct ScheduledMethodDescriptor {
    /// Method name for logging and identification.
    /// 方法名称，用于日志和标识。
    pub name: &'static str,

    /// Scheduling configuration (cron, fixed_rate, or fixed_delay).
    /// 调度配置（cron、fixed_rate 或 fixed_delay）。
    pub schedule_config: ScheduleConfig,

    /// The task function to execute.
    /// 要执行的任务函数。
    pub task_fn: ScheduledFn,
}

/// Task function type stored in the descriptor.
/// 描述符中存储的任务函数类型。
pub type ScheduledFn = Arc<dyn Fn() + Send + Sync + 'static>;

// Collect all scheduled method descriptors at compile time.
// 在编译期收集所有定时方法描述符。
inventory::collect!(ScheduledMethodDescriptor);

