/// A parsed cron expression.
/// 解析后的 Cron 表达式。
///
/// Supports the standard 5-field format: `minute hour day-of-month month day-of-week`.
/// 支持标准 5 字段格式：`分 时 日 月 星期`。
///
/// | Field / 字段        | Allowed values / 允许值 | Special chars / 特殊字符 |
/// |---------------------|------------------------|------------------------|
/// | Minute / 分         | 0-59                   | * , -                  |
/// | Hour / 时           | 0-23                   | * , -                  |
/// | Day of month / 日   | 1-31                   | * , -                  |
/// | Month / 月          | 1-12                   | * , -                  |
/// | Day of week / 星期  | 0-6 (0=Sun)            | * , -                  |
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct CronExpression {
    /// Minute field (0-59).
    /// 分钟字段。
    pub minute: CronField,

    /// Hour field (0-23).
    /// 小时字段。
    pub hour: CronField,

    /// Day of month field (1-31).
    /// 日期字段。
    pub day_of_month: CronField,

    /// Month field (1-12).
    /// 月份字段。
    pub month: CronField,

    /// Day of week field (0-6, 0=Sunday).
    /// 星期字段（0=周日）。
    pub day_of_week: CronField,
}

/// A single cron field representing a set of allowed values.
/// 单个 cron 字段，表示一组允许的值。
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum CronField {
    /// Wildcard — matches all values (`*`).
    /// 通配符 — 匹配所有值。
    All,

    /// A single specific value.
    /// 一个具体的值。
    Exact(u32),

    /// A range of values (inclusive), e.g. `1-5`.
    /// 值范围（含端点）。
    Range(u32, u32),

    /// A list of discrete values, e.g. `1,3,5`.
    /// 离散值列表。
    List(Vec<u32>),

    /// Step values, e.g. `*/5` or `0-30/5`.
    /// 步进值。
    Step {
        /// Base field (what to step from).
        /// 基础字段。
        base: Box<CronField>,
        /// Step size.
        /// 步长。
        step: u32,
    },
}

impl CronField {
    /// Check if a given value matches this field.
    /// 检查给定值是否匹配此字段。
    pub fn matches(&self, value: u32) -> bool {
        match self {
            CronField::All => true,
            CronField::Exact(v) => *v == value,
            CronField::Range(start, end) => value >= *start && value <= *end,
            CronField::List(values) => values.contains(&value),
            CronField::Step { base, step } => {
                if !base.matches(value) {
                    return false;
                }
                // Check if value aligns with step.
                // 检查值是否与步长对齐。
                match base.as_ref() {
                    CronField::All => value % step == 0,
                    CronField::Range(start, _end) => {
                        if value < *start {
                            return false;
                        }
                        (value - start) % step == 0
                    },
                    _ => true,
                }
            },
        }
    }

    /// Get a reference to the inner base, if this is a Step variant.
    /// 获取内部基础的引用。
    fn as_ref(&self) -> &CronField {
        match self {
            CronField::Step { base, .. } => base,
            other => other,
        }
    }
}

/// Errors that can occur during schedule scanning and configuration.
/// 调度扫描和配置过程中可能出现的错误。
#[derive(Debug)]
pub enum ScheduleScanError {
    /// Invalid cron expression.
    /// 无效的 Cron 表达式。
    InvalidCron {
        /// Detail message.
        /// 详细消息。
        detail: String,
    },

    /// Invalid scheduling configuration.
    /// 无效的调度配置。
    InvalidConfig {
        /// Detail message.
        /// 详细消息。
        detail: String,
    },

    /// Multiple conflicting schedule modes specified.
    /// 指定了多个冲突的调度模式。
    ConflictingModes {
        /// The conflicting mode names.
        /// 冲突的模式名称。
        modes: Vec<&'static str>,
    },
}

impl std::fmt::Display for ScheduleScanError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::InvalidCron { detail } => {
                write!(f, "invalid cron expression: {}", detail)
            },
            Self::InvalidConfig { detail } => {
                write!(f, "invalid schedule config: {}", detail)
            },
            Self::ConflictingModes { modes } => {
                write!(f, "conflicting schedule modes: {}", modes.join(", "))
            },
        }
    }
}

impl std::error::Error for ScheduleScanError {}

impl FromStr for CronExpression {
    type Err = ScheduleScanError;

    /// Parse a 5-field cron expression.
    /// 解析 5 字段 cron 表达式。
    ///
    /// Format: `minute hour day_of_month month day_of_week`
    /// 格式：`分 时 日 月 星期`
    ///
    /// Each field supports: `*`, specific values, ranges (`1-5`),
    /// lists (`1,3,5`), and steps (`*/5`, `1-10/2`).
    ///
    /// 每个字段支持：`*`、具体值、范围（`1-5`）、列表（`1,3,5`）和步进（`*/5`、`1-10/2`）。
    fn from_str(input: &str) -> Result<Self, Self::Err> {
        let input = input.trim();
        let fields: Vec<&str> = input.split_whitespace().collect();

        if fields.len() != 5 {
            return Err(ScheduleScanError::InvalidCron {
                detail: format!("expected 5 fields, got {}; expression: '{}'", fields.len(), input),
            });
        }

        Ok(CronExpression {
            minute: parse_cron_field(fields[0], 0, 59, "minute")?,
            hour: parse_cron_field(fields[1], 0, 23, "hour")?,
            day_of_month: parse_cron_field(fields[2], 1, 31, "day_of_month")?,
            month: parse_cron_field(fields[3], 1, 12, "month")?,
            day_of_week: parse_cron_field(fields[4], 0, 6, "day_of_week")?,
        })
    }
}

/// Parse a single cron field.
/// 解析单个 cron 字段。
fn parse_cron_field(
    input: &str,
    min_val: u32,
    max_val: u32,
    field_name: &str,
) -> Result<CronField, ScheduleScanError> {
    let input = input.trim();

    // Handle step syntax: base/step.
    // 处理步进语法：base/step。
    if let Some(slash_pos) = input.find('/') {
        let base_str = &input[..slash_pos];
        let step_str = &input[slash_pos + 1..];

        let step: u32 = step_str
            .parse()
            .map_err(|_| ScheduleScanError::InvalidCron {
                detail: format!("invalid step value '{}' in field '{}'", step_str, field_name),
            })?;

        if step == 0 {
            return Err(ScheduleScanError::InvalidCron {
                detail: format!("step value must be > 0 in field '{}'", field_name),
            });
        }

        let base = if base_str == "*" {
            CronField::All
        } else {
            parse_cron_atom(base_str, min_val, max_val, field_name)?
        };

        return Ok(CronField::Step {
            base: Box::new(base),
            step,
        });
    }

    // Handle list syntax: a,b,c.
    // 处理列表语法：a,b,c。
    if input.contains(',') {
        let mut values = Vec::new();
        for part in input.split(',') {
            let v = parse_single_value(part.trim(), min_val, max_val, field_name)?;
            values.push(v);
        }
        return Ok(CronField::List(values));
    }

    parse_cron_atom(input, min_val, max_val, field_name)
}

/// Parse a single cron atom (no commas, no slash).
/// 解析单个 cron 原子（不含逗号和斜杠）。
fn parse_cron_atom(
    input: &str,
    min_val: u32,
    max_val: u32,
    field_name: &str,
) -> Result<CronField, ScheduleScanError> {
    if input == "*" {
        return Ok(CronField::All);
    }

    if let Some(dash_pos) = input.find('-') {
        let start_str = &input[..dash_pos];
        let end_str = &input[dash_pos + 1..];

        let start: u32 = start_str
            .parse()
            .map_err(|_| ScheduleScanError::InvalidCron {
                detail: format!("invalid range start '{}' in field '{}'", start_str, field_name),
            })?;

        let end: u32 = end_str
            .parse()
            .map_err(|_| ScheduleScanError::InvalidCron {
                detail: format!("invalid range end '{}' in field '{}'", end_str, field_name),
            })?;

        validate_range(start, end, min_val, max_val, field_name)?;
        return Ok(CronField::Range(start, end));
    }

    let v = parse_single_value(input, min_val, max_val, field_name)?;
    Ok(CronField::Exact(v))
}

/// Parse a single numeric value from a cron field.
/// 从 cron 字段解析单个数值。
fn parse_single_value(
    input: &str,
    min_val: u32,
    max_val: u32,
    field_name: &str,
) -> Result<u32, ScheduleScanError> {
    let v: u32 = input.parse().map_err(|_| ScheduleScanError::InvalidCron {
        detail: format!("invalid value '{}' in field '{}'", input, field_name),
    })?;

    if v < min_val || v > max_val {
        return Err(ScheduleScanError::InvalidCron {
            detail: format!(
                "value {} out of range [{}, {}] in field '{}'",
                v, min_val, max_val, field_name
            ),
        });
    }

    Ok(v)
}

/// Validate that a range is within allowed bounds.
/// 验证范围在允许边界内。
fn validate_range(
    start: u32,
    end: u32,
    min_val: u32,
    max_val: u32,
    field_name: &str,
) -> Result<(), ScheduleScanError> {
    if start > end {
        return Err(ScheduleScanError::InvalidCron {
            detail: format!("range start {} > end {} in field '{}'", start, end, field_name),
        });
    }

    if start < min_val || end > max_val {
        return Err(ScheduleScanError::InvalidCron {
            detail: format!(
                "range [{}..{}] out of bounds [{}, {}] in field '{}'",
                start, end, min_val, max_val, field_name
            ),
        });
    }

    Ok(())
}

